package application;

public class ModelBlok {
    Integer no;
    String blok;

    public ModelBlok(Integer no, String blok) {
        this.no = no;
        this.blok = blok;
    }

    public Integer getNo() {
        return no;
    }

    public void setNo(Integer no) {
        this.no = no;
    }

    public String getBlok() {
        return blok;
    }

    public void setBlok(String blok) {
        this.blok = blok;
    }
}
